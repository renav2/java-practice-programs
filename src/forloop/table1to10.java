public class table1to10 {
    public static void main(String args[]){
    
       for(int a=1;a<=10;a++){
        for(int i=1;i<=10;i++){
            System.out.println(a+" * "+i+" = "+a*i);
        }
           System.out.println("");
       }
    
    }
}
